#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const char	*ptr;
	size_t		i;

	if (n > 0)
	{
		i = 0;
		ptr = s;
		while (i < n)
		{
			if (ptr[i] == c)
			{
				return ((void *)ptr + i);
			}
			i++;
		}
	}
	return (0);
}
