#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*ptr;

	ptr = ft_memalloc(size + 1);
	if (ptr == NULL)
		return (0);
	ft_bzero(ptr, size + 1);
	return (ptr);
}
