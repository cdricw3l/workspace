#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	n;
	int	result;

	i = 0;
	result = 0;
	n = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == 43 || str[i] == 45)
	{
		if (str[i] == 45)
			n++;
		i++;
	}
	while (ft_isdigit(str[i]))
	{
		result = result * 10 + (str[i] - '0');
		i++;
	}
	if (n % 2 != 0)
		return (-(result));
	return (result);
}
